package ScacchiX4;

/********************************************************
 *@title COPPIA
 *@package ScacchiX4
 *@brief 
********************************************************/

public class Coppia {

    private final int x;
    private final int y;

    /*******************************************
    *COPPIA
    *@brief 
    *******************************************/
    public Coppia(int x, int y) {
        this.x = x;
        this.y = y;
    }

    /*******************************************
    *GET X
    *@brief 
    *******************************************/
    public int getX() {
        return x;
    }

    /*******************************************
    *GET Y
    *@brief 
    *******************************************/
    public int getY() {
        return y;
    }
}
